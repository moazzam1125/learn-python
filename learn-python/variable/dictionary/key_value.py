''' key-value methods in dictionary '''

# Keys

key_dictionary = {
    'sector': 'Keys method',
    'variable': 'dictionary'
}
print(key_dictionary.keys())

# Values

value_dictionary = {
    'sector': 'Values method',
    'variable': 'dictionary'
}
print(value_dictionary.values())

# Key-value

key_value_dictionary = {
    'sector': 'key-value method',
    'variable': 'dictionary'
}
print(key_value_dictionary.items())